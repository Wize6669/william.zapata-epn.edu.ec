    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        try {
            Class.forName(driver);
            con =(Connection) DriverManager.getConnection(url);
            s = (PreparedStatement) con.prepareStatement("Inser into estudiante values(?,?,?,?,?,?,?)");
            s.setString(1,jTFNombre.getText());
            s.setString(2,jTFCarrera.getText());
            s.setString(3,jTFCodigoUnico.getText());
            s.setString(4,jTFDireccion.getText());
            s.setString(5,jTFEdad.getText());
            s.setString(6,jTFEmail.getText());
            s.setString(7,jTFTelefono.getText());
            s.executeUpdate();
            JOptionPane.showMessageDialog(null,"Datos guardados con exito perro");
        } catch (ClassNotFoundException ex) {
        JOptionPane.showConfirmDialog(null, ex);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null,ex);
    }
    }//GEN-LAST:event_jBGuardarActionPerformed

